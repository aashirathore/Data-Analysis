
public class DataByAge {
	String age;
	int count;
	int totalAnnualIncome;
	int averageSpendingScore;
	public String getAge() {
		return age;
	}
	public void setAge(String age) {
		this.age = age;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public int getTotalAnnualIncome() {
		return totalAnnualIncome;
	}
	public void setTotalAnnualIncome(int totalAnnualIncome) {
		this.totalAnnualIncome = totalAnnualIncome;
	}
	public int getAverageSpendingScore() {
		return averageSpendingScore;
	}
	public void setAverageSpendingScore(int averageSpendingScore) {
		this.averageSpendingScore = averageSpendingScore;
	}
}
